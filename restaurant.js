var Receta = function (id, nombre, dificultad, rubro, tipo, ingredientes, imagen, descarga) {
    this.id = id;
    this.nombre = nombre;
    this.dificultad = dificultad;
    this.rubro = rubro;
    this.tipo = tipo;
    this.ingredientes = ingredientes;
    this.imagen = imagen;
    this.descarga = descarga;
}

